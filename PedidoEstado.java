/**
 * El enumerado PedidoEstado representa los posibles estados de un pedido.
 * 
 * Esta enumeración define dos valores constantes: PENDIENTE y ENTREGADO, 
 * que indican los estados en los que puede encontrarse un pedido.
 * 
 * PENDIENTE: Indica que el pedido está pendiente de ser entregado.
 * ENTREGADO: Indica que el pedido ha sido entregado satisfactoriamente.
 * 
 * Esta enumeración se utiliza para gestionar y seguir el estado de los pedidos.
 * 
 * Autor: Alexandre Insua Moreira
 * Versión: 1.0.
 */
public enum PedidoEstado
{
    PENDIENTE, ENTREGADO;
}
